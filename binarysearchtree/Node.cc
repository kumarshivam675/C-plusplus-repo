#include"Node.h"
#include <cstddef>

using namespace std;

Node::Node(){
	left = NULL;
	right = NULL;
}

Node::Node(int val){
	value = val;
	left = NULL;
	right = NULL;
}

int Node::getValue(){
	return value;
}

void Node::setValue(int val){
	value = val;
}

Node* Node::getLeft(){
	return left;
}

Node* Node::getRight(){
	return right;
}

void Node::setLeft(Node* newnode){
	left = newnode;
}

void Node::setRight(Node* newnode){
	right = newnode;
}

